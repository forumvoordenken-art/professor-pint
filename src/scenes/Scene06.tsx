const DebrisSystem: React.FC<{ frame: number; triggerFrame: number }> = ({ frame, triggerFrame }) => {
  const particles = useMemo<Debris[]>(() => {
    const rand = seededRandom(8888);
    return new Array(200).fill(0).map((_, id) => {
      const angle = rand() * Math.PI * 2;
      const speed = 8 + rand() * 26;
      const radius = 2 + rand() * 6;
      const kind = rand() > 0.4 ? 'dust' : 'stone';
      return { id, angle, speed, radius, kind };
    });
  }, []);

  const t = clamp(frame - triggerFrame, 0, 90);

  return (
    <g>
      {particles.map((particle) => {
        const travel = interpolate(t, [0, 90], [0, particle.speed * 12], { extrapolateRight: 'clamp' });
        const gravity = 0.2 * t * t;
        const x = 960 + Math.cos(particle.angle) * travel;
        const y = 540 + Math.sin(particle.angle) * travel + gravity;
        const fade = interpolate(t, [0, 70, 90], [0.96, 0.52, 0], { extrapolateRight: 'clamp' });
        return particle.kind === 'dust' ? (
          <circle
            key={`debris-d-${particle.id}`}
            cx={x}
            cy={y}
            r={particle.radius}
            fill="#d8c9b2"
            opacity={fade * 0.6}
          />
        ) : (
          <rect
            key={`debris-s-${particle.id}`}
            x={x - particle.radius}
            y={y - particle.radius}
            width={particle.radius * 2.2}
            height={particle.radius * 1.6}
            fill="#7d6a55"
            opacity={fade}
            transform={`rotate(${particle.id * 17} ${x} ${y})`}
          />
        );
      })}
    </g>
  );
};

