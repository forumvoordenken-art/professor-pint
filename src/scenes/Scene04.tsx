const fractalEdge = (x1: number, y1: number, x2: number, y2: number, depth: number, jitter: number): Array<{ x1: number; y1: number; x2: number; y2: number }> => {
  if (depth <= 0) return [{ x1, y1, x2, y2 }];
  const midX = (x1 + x2) / 2 + Math.sin((x1 + y2) * 0.01) * jitter;
  const midY = (y1 + y2) / 2 + Math.cos((x2 + y1) * 0.01) * jitter;
  const left = fractalEdge(x1, y1, midX, midY, depth - 1, jitter * 0.62);
  const right = fractalEdge(midX, midY, x2, y2, depth - 1, jitter * 0.62);
  return [...left, ...right];
};

